// Retorna um array a partir de outro array
// O array retornado é determinado pelos parâmetros que são os índices de início e fim
// do novo array


let nums = [1, 3, 6, 5, 4, 1, 2, 1, 4, 5];

console.log(nums.slice(2)); //pega o segundo elemento 
console.log(nums.slice(4,5)); //pega o quarto elemento 
console.log(nums.slice(4, 6)); //pega o quarto e o quinto elementos
console.log(nums.slice(-3)); //pega os 3 ultimos numeros do array 
console.log(nums.slice(1, -2)); //remove os 2 ultimos numeros do array 



